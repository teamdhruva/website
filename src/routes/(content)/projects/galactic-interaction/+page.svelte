    <h1 class="text-2xl font-semibold">Goals</h1>
    <p>
      To analyse galaxy interactions and star clusters in these interacting
      galaxies using image processing and study their characteristic features.
    </p>

    <hr class="mt-4 mb-4" />

    <p>
      Star clusters are groups of stars that are gravitationally bound and
      formed from the same molecular cloud. They are important for studying
      stellar evolution and the formation of galaxies.
    </p>
    <p>
      At dhRuVa, we use image processing techniques, photometry and astronomical
      tests to analyze images of galaxies interacting with star clusters. We
      primarily identify patterns and anomalies. Stacking and photometry can
      help us look for characteristic features of the galaxies. Specific
      photometric filters help in estimating the age and shape of galaxies.
      Analysing star clusters can give insights into the composition of stars
      and time of their formation.
    </p>
